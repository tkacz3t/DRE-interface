package edu.gwu.audio;

public class AudioCaptureException extends RuntimeException {
	public AudioCaptureException() {
		super();
	}

	public AudioCaptureException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AudioCaptureException(String arg0) {
		super(arg0);
	}

	public AudioCaptureException(Throwable arg0) {
		super(arg0);
	}

}
